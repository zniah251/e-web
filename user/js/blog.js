  // ----------------- Hiển thị chi tiết bài viết (trang post.html) -----------------
  function renderPostDetail() {
    const urlParams = new URLSearchParams(window.location.search);
    const postId = parseInt(urlParams.get("id"));
  
    const post = blogPosts.find(p => p.id === postId);
    if (post) {
      document.getElementById("title").innerText = post.title;
      document.getElementById("meta").innerText = `${post.category} / ${post.date}`;
      document.getElementById("image").src = post.image;
      document.getElementById("image").alt = post.title;
      document.getElementById("content").innerHTML = post.content;
    } else {
      document.getElementById("title").innerText = "Không tìm thấy bài viết.";
    }
  }
  
  
  function renderBlogList(filteredPosts = blogPosts) {
    const blogContainer = document.getElementById("blog-container");
    if (!blogContainer) return;
  
    blogContainer.innerHTML = ""; // clear old content
  
    filteredPosts.forEach(post => {
      const postItem = document.createElement("div");
      postItem.className = "post-item";
  
      postItem.innerHTML = `
        <div class="post-image">
          <a href="post.html?id=${post.id}">
            <img src="${post.image}" alt="${post.title}" />
          </a>
        </div>
        <div class="post-meta">${post.category} / ${post.date}</div>
        <div class="post-title">
          <a href="post.html?id=${post.id}">${post.title}</a>
        </div>
        <div class="post-content">
          <p>${post.content.replace(/<[^>]+>/g, "").slice(0, 150)}...</p>
        </div>
      `;
  
      blogContainer.appendChild(postItem);
    });
  }
  
  function filterByGender(gender) {
    const filtered = blogPosts.filter(post => post.gender === gender);
    renderBlogList(filtered);
  }

